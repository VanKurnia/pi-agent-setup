/**
 * Check if a bash command is safe (read-only and not destructive).
 * Returns `true` if the command can be allowed in plan mode.
 */
export function isSafeCommand(command: string): boolean {
	const isDestructive = DESTRUCTIVE_PATTERNS.some((p) => p.test(command));
	const isSafe = SAFE_PATTERNS.some((p) => p.test(command));
	return !isDestructive && isSafe;
}

/**
 * A single todo item extracted from a "Plan:" section.
 */
export interface TodoItem {
	/** 1-based step number */
	step: number;
	/** Human-readable step description */
	text: string;
	/** Whether the step has been marked done via [DONE:n] */
	completed: boolean;
}

/**
 * Clean up a step text: remove markdown formatting, leading verbs, truncate.
 */
export function cleanStepText(text: string): string {
	let cleaned = text
		.replace(/\*{1,2}([^*]+)\*{1,2}/g, "$1") // Remove bold/italic
		.replace(/`([^`]+)`/g, "$1") // Remove code backticks
		.replace(
			/^(Use|Run|Execute|Create|Write|Read|Verify|Update|Modify|Add|Remove|Delete|Install)\s+/i,
			"",
		)
		.replace(/\s+/g, " ")
		.trim();

	if (cleaned.length > 0) {
		cleaned = cleaned.charAt(0).toUpperCase() + cleaned.slice(1);
	}
	if (cleaned.length > 50) {
		cleaned = cleaned.slice(0, 47) + "...";
	}
	return cleaned;
}

/**
 * Extract numbered todo items from a "Plan:" section in a message.
 * Looks for `Plan:` header (case-insensitive, optional markdown bold),
 * then numbered items like `1. ...` or `1) ...`.
 */
export function extractTodoItems(message: string): TodoItem[] {
	const items: TodoItem[] = [];
	const headerMatch = message.match(/\*{0,2}Plan:\*{0,2}\s*\n/i);
	if (!headerMatch) return items;

	const planSection = message.slice(message.indexOf(headerMatch[0]) + headerMatch[0].length);
	const numberedPattern = /^\s*(\d+)[.)]\s+\*{0,2}(.*?)\*{0,2}$/gm;

	for (const match of planSection.matchAll(numberedPattern)) {
		const text = match[2]
			.trim()
			.replace(/\*{1,2}/g, "")
			.trim();
		if (!text.startsWith("`") && !text.startsWith("/") && !text.startsWith("-")) {
			const cleaned = cleanStepText(text);
			if (cleaned.length > 3) {
				items.push({ step: items.length + 1, text: cleaned, completed: false });
			}
		}
	}
	return items;
}

/**
 * Extract step numbers from `[DONE:n]` markers in text.
 */
export function extractDoneSteps(message: string): number[] {
	const steps: number[] = [];
	for (const match of message.matchAll(/\[DONE:(\d+)\]/gi)) {
		const step = Number(match[1]);
		if (Number.isFinite(step)) steps.push(step);
	}
	return steps;
}

/**
 * Scan text for `[DONE:n]` markers and mark matching items as completed.
 * Returns the number of newly completed items.
 */
export function markCompletedSteps(text: string, items: TodoItem[]): number {
	const doneSteps = extractDoneSteps(text);
	let count = 0;
	for (const step of doneSteps) {
		const item = items.find((t) => t.step === step);
		if (item && !item.completed) {
			item.completed = true;
			count++;
		}
	}
	return count;
}
